package loop;

public class whileExam {
    public static void main(String[]args){
        int i =0;
        int sum=0;
        while(i<=10){
            sum += i;
            System.out.println("i의 값은 : "+i);
            System.out.println("sum + i의 값은 : "+sum);
            i++;

        }
    }
}
